"""
Write a Python script that takes input from the user and displays that input back in upper and lower cases. 
"""
string_input = str(input("Input a literal Text: \n"))
print("Your input is:\n",string_input, "\n")
print("Upper case of the input is:\n",string_input.upper(), "\n")
print("Lower Case of the input is:\n",string_input.lower(), "\n")